<h1>Vision, Mission, Direction</h1>
<p>
    Unity in angklung subsequently gives birth to what we call our ensemble as a
    “famsemble” - a portmanteau of family and ensemble. We believe that as an
    NUS Angklung family, it is our responsibility to “remind one another, care
    for one another, and guide one another”.
</p>

<p>
    NUS Angklung Ensemble showcases angklung’s versatility that balances
    traditionality and modernity through performing pieces that cater to current
    popular trends.
</p>

<h2>Vision</h2>
<p>
    A community where everyone can learn, share, and guide each other through
    learning Angklung.
</p>

<h2>Mission</h2>
<p>
    From its establishment in 2007, NUS Angklung Ensemble strives to provide a
    safe and inclusive space for the NUS community to freely express themselves
    through Nusantara instruments, while simultaneously promoting Indonesian
    heritage through Angklung to increase cultural intelligence and tolerance.
</p>

<h2>Direction</h2>
<ol>
    <li>Creating a closely-knit “famsemble”</li>
    <li>Establishing the club’s presence towards the wider NUS community</li>
</ol>
